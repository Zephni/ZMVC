<p>
What is the MVC Pattern?
<ul>
	<li>Model: Structures your data in a reliable form and prepares it based on controller’s instructions</li>
	<li>View: Displays data to user in easy-to-understand format, based on the user’s actions</li>
	<li>Controller: Takes in user commands, sends commands to the model for data updates, sends instructions to view to update interface.</li>
</ul>
</p>

<p>ZMVC works slightly different than your usual though... Rather than having to build classes to arrange your code, it just has to be put in the correct place. 
The controller works by searching for the correct model, loading it in, and then launching the correct view based on that model, however the model is not nessesary.
